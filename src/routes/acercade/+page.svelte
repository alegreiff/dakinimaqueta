<h1>Acerca de Dakini</h1>
<h2>¿Por qué <strong>Dakini</strong> ?</h2>
<p>
	Las dakinis son seres energéticos en forma femenina, que evocan el movimiento de la energía en el
	espacio.
</p>
<p>En tibetano significa bailarina del cielo, la que se mueve por el espacio infinito.</p>
<p>
	Personifica la transformación, la transmutación, el movimiento de la ira y la resistencia
	reprimida y la negación hacia una poderosa sabiduría y feroz compasión.
</p>
<p>
	Las metáforas de la danza impregnan los arquetipos budistas de la iluminación tanto en el arte
	como en el texto porque demuestran ideales como el dinamismo, la fluidez, la estabilidad, la
	energía feroz y el equilibrio.
</p>
